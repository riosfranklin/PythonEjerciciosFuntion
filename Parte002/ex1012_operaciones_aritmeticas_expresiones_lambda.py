# Ejercicio 1012: Definir las operaciones aritméticas básicas utilizando expresiones lambda.

sumar = lambda x, y: x + y
restar = lambda x, y: x - y
multiplicar = lambda x, y: x * y
dividir = lambda x, y: x / y

print(sumar(2, 3))  # 5
print(restar(2, 3)) # -1
print(multiplicar(2, 3)) # 6
print(dividir(2, 3)) # ~0.6666
